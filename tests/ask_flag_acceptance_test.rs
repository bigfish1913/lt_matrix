/// Acceptance Criterion 9: Multiple questions can be managed
#[test]
fn test_ac9_multiple_question_management() {
    let mut session = ClarificationSession::new("build full stack app");

    // Add multiple questions
    session.add_question(ClarificationQuestion {
        id: "frontend".to_string(),
        question_text: "Frontend framework?".to_string(),
        question_type: QuestionType::Choice,
        options: Some(vec!["React".to_string(), "Vue".to_string(), "Svelte".to_string()]),
        default_value: None,
        required: true,
        multi_select: false,
    });

    session.add_question(ClarificationQuestion {
        id: "backend".to_string(),
        question_text: "Backend framework?".to_string(),
        question_type: QuestionType::Choice,
        options: Some(vec![
            "Express".to_string(),
            "Django".to_string(),
            "Rails".to_string(),
        ]),
        default_value: None,
        required: true,
        multi_select: false,
    });

    session.add_question(ClarificationQuestion {
        id: "database".to_string(),
        question_text: "Database?".to_string(),
        question_type: QuestionType::Choice,
        options: Some(vec![
            "PostgreSQL".to_string(),
            "MySQL".to_string(),
            "MongoDB".to_string(),
        ]),
        default_value: Some("PostgreSQL".to_string()),
        required: false,
        multi_select: false,
    });

    // Answer required questions
    session.answer_question("frontend", "React").unwrap();
    session.answer_question("backend", "Express").unwrap();

    // Should be able to complete without optional
    assert!(session.all_required_answered());

    // Should track unanswered correctly
    let unanswered = session.unanswered_required();
    assert_eq!(unanswered.len(), 0);
}

