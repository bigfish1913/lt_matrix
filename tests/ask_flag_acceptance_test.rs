/// Acceptance Criterion 4: Questions can be required or optional
#[test]
fn test_ac4_questions_support_required_and_optional() {
    let mut session = ClarificationSession::new("test");

    // Add required question
    session.add_question(ClarificationQuestion {
        id: "required_q".to_string(),
        question_text: "Required question".to_string(),
        question_type: QuestionType::Text,
        options: None,
        default_value: None,
        required: true,
        multi_select: false,
    });

    // Add optional question with default
    session.add_question(ClarificationQuestion {
        id: "optional_q".to_string(),
        question_text: "Optional question".to_string(),
        question_type: QuestionType::Text,
        options: None,
        default_value: Some("default".to_string()),
        required: false,
        multi_select: false,
    });

    // Required question should prevent completion without answer
    assert!(!session.all_required_answered());

    // Answering required question should allow completion
    session.answer_question("required_q", "answer").unwrap();
    assert!(session.all_required_answered());
}

