/// Acceptance Criterion 7: Answers are injected into planning prompt
#[test]
fn test_ac7_answers_injected_into_planning_prompt() {
    let mut session = ClarificationSession::new("add authentication");

    session.add_question(ClarificationQuestion {
        id: "auth_method".to_string(),
        question_text: "Which authentication method?".to_string(),
        question_type: QuestionType::Choice,
        options: Some(vec![
            "JWT".to_string(),
            "OAuth 2.0".to_string(),
            "Session".to_string(),
        ]),
        default_value: None,
        required: true,
        multi_select: false,
    });

    session.add_question(ClarificationQuestion {
        id: "additional_features".to_string(),
        question_text: "Any additional features?".to_string(),
        question_type: QuestionType::Text,
        options: None,
        default_value: None,
        required: false,
        multi_select: false,
    });

    session.answer_question("auth_method", "JWT").unwrap();
    session
        .answer_question("additional_features", "Password reset flow")
        .unwrap();

    let injection = session.generate_prompt_injection();

    // Should contain clarification header
    assert!(injection.contains("User Clarifications"));

    // Should contain question and answer
    assert!(injection.contains("Which authentication method?"));
    assert!(injection.contains("JWT"));

    // Should contain optional question and answer
    assert!(injection.contains("Any additional features?"));
    assert!(injection.contains("Password reset flow"));

    // Should contain footer
    assert!(injection.contains("End Clarifications"));
}

