#[cfg(test)]
mod session_validation_tests {
    use ltmatrix::interactive::clarify::{
        ClarificationQuestion, ClarificationSession, QuestionType,
    };

    /// Test that unanswered required questions are detected
    #[test]
    fn test_detect_unanswered_required_questions() {
        let mut session = ClarificationSession::new("test");

        session.add_question(ClarificationQuestion {
            id: "q1".to_string(),
            question_text: "Q1".to_string(),
            question_type: QuestionType::Text,
            options: None,
            default_value: None,
            required: true,
            multi_select: false,
        });

        session.add_question(ClarificationQuestion {
            id: "q2".to_string(),
            question_text: "Q2".to_string(),
            question_type: QuestionType::Text,
            options: None,
            default_value: None,
            required: true,
            multi_select: false,
        });

        session.answer_question("q1", "answer").unwrap();

        let unanswered = session.unanswered_required();
        assert_eq!(unanswered.len(), 1);
        assert_eq!(unanswered[0].id, "q2");
    }

    /// Test skipping optional question with default
    #[test]
    fn test_skip_optional_with_default() {
        let mut session = ClarificationSession::new("test");

        session.add_question(ClarificationQuestion {
            id: "optional".to_string(),
            question_text: "Optional?".to_string(),
            question_type: QuestionType::Text,
            options: None,
            default_value: Some("default_value".to_string()),
            required: false,
            multi_select: false,
        });

        session.skip_question("optional").unwrap();

        assert_eq!(
            session.answers.get("optional"),
            Some(&"default_value".to_string())
        );
    }

    /// Test that skipping required question fails
    #[test]
    fn test_cannot_skip_required_without_default() {
        let mut session = ClarificationSession::new("test");

        session.add_question(ClarificationQuestion {
            id: "required".to_string(),
            question_text: "Required?".to_string(),
            question_type: QuestionType::Text,
            options: None,
            default_value: None,
            required: true,
            multi_select: false,
        });

        let result = session.skip_question("required");
        assert!(result.is_err(), "Should not skip required question");
    }

    /// Test answering non-existent question fails
    #[test]
    fn test_answer_nonexistent_question_fails() {
        let mut session = ClarificationSession::new("test");

        let result = session.answer_question("nonexistent", "answer");
        assert!(result.is_err());
    }

    /// Test duplicate question replacement
    #[test]
    fn test_duplicate_question_replaces_existing() {
        let mut session = ClarificationSession::new("test");

        session.add_question(ClarificationQuestion {
            id: "duplicate".to_string(),
            question_text: "First version".to_string(),
            question_type: QuestionType::Text,
            options: None,
            default_value: None,
            required: true,
            multi_select: false,
        });

        session.add_question(ClarificationQuestion {
            id: "duplicate".to_string(),
            question_text: "Second version".to_string(),
            question_type: QuestionType::Choice,
            options: Some(vec!["A".to_string(), "B".to_string()]),
            default_value: None,
            required: true,
            multi_select: false,
        });

        assert_eq!(session.questions.len(), 1);
        assert_eq!(session.questions[0].question_text, "Second version");
        assert!(matches!(
            session.questions[0].question_type,
            QuestionType::Choice
        ));
    }
}

#[cfg(test)]
mod edge_case_tests {
    use ltmatrix::interactive::clarify::{
        ClarificationQuestion, ClarificationSession, QuestionType,
    };

    /// Test empty goal
    #[test]
    fn test_empty_goal_handling() {
        let session = ClarificationSession::new("");
        assert_eq!(session.goal, "");
    }

    /// Test goal with special characters
    #[test]
    fn test_special_characters_in_goal() {
        let goal = "Add feature: \"quoted\" and 'apostrophes' & symbols!";
        let session = ClarificationSession::new(goal);
        assert_eq!(session.goal, goal);
    }

    /// Test very long goal
    #[test]
    fn test_very_long_goal() {
        let goal = "a".repeat(10000);
        let session = ClarificationSession::new(&goal);
        assert_eq!(session.goal.len(), 10000);
    }

    /// Test session with no questions
    #[test]
    fn test_session_with_no_questions() {
        let session = ClarificationSession::new("clear goal");
        assert!(session.questions.is_empty());
        assert!(session.all_required_answered());
        assert!(session.unanswered_required().is_empty());
    }

    /// Test question with very long text
    #[test]
    fn test_very_long_question_text() {
        let long_text = "Question".repeat(1000);
        let question = ClarificationQuestion {
            id: "long_q".to_string(),
            question_text: long_text,
            question_type: QuestionType::Text,
            options: None,
            default_value: None,
            required: true,
            multi_select: false,
        };

        assert_eq!(question.question_text.len(), 8000);
    }

    /// Test question with many options
    #[test]
    fn test_question_with_many_options() {
        let options: Vec<String> = (1..=100).map(|i| format!("Option {}", i)).collect();

        let question = ClarificationQuestion {
            id: "many_options".to_string(),
            question_text: "Choose one".to_string(),
            question_type: QuestionType::Choice,
            options: Some(options),
            default_value: None,
            required: true,
            multi_select: false,
        };

        assert_eq!(question.options.as_ref().unwrap().len(), 100);
    }

    /// Test answer with special characters
    #[test]
    fn test_answer_with_special_characters() {
        let mut session = ClarificationSession::new("test");

        session.add_question(ClarificationQuestion {
            id: "q1".to_string(),
            question_text: "Question?".to_string(),
            question_type: QuestionType::Text,
            options: None,
            default_value: None,
            required: true,
            multi_select: false,
        });

        let special_answer =
            "Answer with 'quotes', \"double quotes\", & symbols, and\nnewlines\ttabs";
        session.answer_question("q1", special_answer).unwrap();

        assert_eq!(session.answers.get("q1"), Some(&special_answer.to_string()));
    }

    /// Test clear goal generates minimal questions
    #[test]
    fn test_clear_goal_generates_minimal_questions() {
        let goal =
            "Add unit tests for the UserService.login() method using mockito for HTTP mocking";
        let session = ClarificationSession::new(goal);
        assert_eq!(session.goal, goal);
    }

    /// Test prompt injection with no answers
    #[test]
    fn test_prompt_injection_with_no_answers() {
        let session = ClarificationSession::new("test");
        let injection = session.generate_prompt_injection();

        assert!(
            injection.is_empty(),
            "Should return empty string when no answers"
        );
    }

    /// Test multi-select answer format
    #[test]
    fn test_multiselect_answer_format() {
        let mut session = ClarificationSession::new("test");

        session.add_question(ClarificationQuestion {
            id: "features".to_string(),
            question_text: "Select features".to_string(),
            question_type: QuestionType::MultiSelect,
            options: Some(vec!["A".to_string(), "B".to_string(), "C".to_string()]),
            default_value: None,
            required: true,
            multi_select: true,
        });

        // Multi-select answers stored as comma-separated string
        session.answer_question("features", "A, C").unwrap();

        assert_eq!(session.answers.get("features"), Some(&"A, C".to_string()));
    }
}
