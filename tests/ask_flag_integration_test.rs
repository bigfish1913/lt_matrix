//! Integration tests for --ask flag (interactive mode)
//!
//! This test suite verifies the integration of the --ask flag
//! with the CLI, pipeline, and user interaction flows.

use clap::Parser;
use ltmatrix::cli::args::Args;

#[cfg(test)]
mod cli_integration_tests {
    use super::*;

    /// Test that --ask flag is properly parsed by CLI
    #[test]
    fn test_ask_flag_parsing() {
        let args = Args::parse_from(["ltmatrix", "--ask", "add authentication"]);

        assert!(args.ask, "ask flag should be true");
        assert_eq!(args.goal, Some("add authentication".to_string()));
    }

    /// Test that --ask works with other flags
    #[test]
    fn test_ask_flag_with_other_options() {
        let args = Args::parse_from([
            "ltmatrix",
            "--ask",
            "--fast",
            "--output",
            "json",
            "build API",
        ]);

        assert!(args.ask, "ask flag should be true");
        assert!(args.fast, "fast flag should be true");
        assert_eq!(args.goal, Some("build API".to_string()));
    }

    /// Test that --ask flag defaults to false
    #[test]
    fn test_ask_flag_default() {
        let args = Args::parse_from(["ltmatrix", "test goal"]);

        assert!(!args.ask, "ask flag should default to false");
    }

    /// Test --ask with dry-run mode
    #[test]
    fn test_ask_flag_with_dry_run() {
        let args = Args::parse_from(["ltmatrix", "--ask", "--dry-run", "test goal"]);

        assert!(args.ask, "ask flag should be true");
        assert!(args.dry_run, "dry-run flag should be true");
    }

    /// Test --ask with config file
    #[test]
    fn test_ask_flag_with_config() {
        let args = Args::parse_from(["ltmatrix", "--ask", "--config", "custom.toml", "test goal"]);

        assert!(args.ask, "ask flag should be true");
        assert_eq!(args.config, Some(std::path::PathBuf::from("custom.toml")));
    }
}

